    private void forgotBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_forgotBtnActionPerformed
        String email = emailTxt.getText().trim();

        if (email.equals("Enter ur Email") || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email first.");
            return;
        }

        try {
            Connection con = Connexion.Connexion.getConnection();

            // Check if email exists
            PreparedStatement ps = con.prepareStatement("SELECT * FROM user WHERE email=?");
            ps.setString(1, email);
            ResultSet rs = ps.executeQuery();

            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "This email is not registered.");
                return;
            }

            // Generate temporary password
            String tempPass = Long.toHexString(Double.doubleToLongBits(Math.random())).substring(0, 8);

            // Update password using SQL SHA1
            PreparedStatement ps2 = con.prepareStatement(
                "UPDATE user SET password = SHA1(?) WHERE email = ?"
            );
            ps2.setString(1, tempPass);   // plain password
            ps2.setString(2, email);
            ps2.executeUpdate();

            // Send the password email
            sendEmail(email, tempPass);

            JOptionPane.showMessageDialog(this,
                    "A new password has been sent to your email.\nUse it to log in.");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error resetting password.");
        }
    }//GEN-LAST:event_forgotBtnActionPerformed

