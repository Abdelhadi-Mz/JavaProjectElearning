    public void sendEmail(String toEmail, String newPassword) {

    final String fromEmail = "elmezouariabdelhadi@gmail.com"; 
    final String fromPassword = "lfqoguspsrrnyjub"; // Gmail App Password

    java.util.Properties props = new java.util.Properties();
    props.put("mail.smtp.host", "smtp.gmail.com");
    props.put("mail.smtp.port", "587");
    props.put("mail.smtp.auth", "true");
    props.put("mail.smtp.starttls.enable", "true");

    javax.mail.Session session = javax.mail.Session.getInstance(props,
        new javax.mail.Authenticator() {
            @Override
            protected javax.mail.PasswordAuthentication getPasswordAuthentication() {
                return new javax.mail.PasswordAuthentication(fromEmail, fromPassword);
            }
        }
    );

    try {
        javax.mail.Message msg = new javax.mail.internet.MimeMessage(session);
        msg.setFrom(new javax.mail.internet.InternetAddress(fromEmail, "E-Learning App"));
        msg.setRecipient(javax.mail.Message.RecipientType.TO,
                new javax.mail.internet.InternetAddress(toEmail));
        msg.setSubject("Password Reset");
        msg.setText("Your new temporary password is: " + newPassword +
                    "\n\nPlease log in and change it after you sign in.");

        javax.mail.Transport.send(msg);

    } catch (Exception e) {
        e.printStackTrace();
    }
}

    
